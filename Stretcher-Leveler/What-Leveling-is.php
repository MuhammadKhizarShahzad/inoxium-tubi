            <h1>What Leveling is</h1><br>
            <p class="text-sm">
                Over the years, much has been written regarding the principles of leveling, and how to get material
                flat.
                In addition to simply getting material flat, more recently there has been a greater emphasis placed on
                developing technology to assure the material stays flat after subsequent processes such as laser or
                plasma cutting, welding, and/or other various forming and fabricating operations. All too often,
                material that appears to be flat doesn’t stay that way. Today, we understand that this is primarily due
                to the existence of randomly trapped internal stresses. While a flat piece of material may appear to be
                relaxed and at rest, in reality there is often a “tug of war” of epic proportions being waged right
                before our eyes. Trapped internal stresses can be introduced in a host of areas or operations. Typically
                these random stresses are at least in part initially introduced at the mill during the rolling process.
                Some portions of the strip are worked more than others. In addition, as the material cools, the outer
                wraps of the coil will cool at different rates than the inner wraps. Subsequent exposure to significant
                temperature changes can also induce additional changes in the material, such as coils that are stored in
                a heated environment, shipped during the cold winter months, and subsequently allowed to warm in the
                next facility. Because the material expands and contracts at different rates, additional stresses can be
                introduced. The leveling process itself, while having the ability to produce flat material, can also
                induce randomly trapped stresses. Because no one single process or condition is solely responsible,
                trapped internal stresses and their consequences are inevitable. While there is a general understanding
                and acceptance regarding the common principles of leveling, there is considerable debate as to how or if
                one particular type of Leveler or leveling process is really better than another at specifically
                addressing the problem of trapped internal stresses and spring back in addition to simply producing flat
                material. Understanding this tug of war will ultimately reveal clear commonsense reasons as to why some
                systems are clearly more effective than others.
            </p><br>
            <h3>FLATNESS AND STRESS</h3>
            <div class="flex">
                <div class="w-2/3 pr-2">
                    <p class="text-sm">While flatness is frequently referred to as a side to side length differential,
                        that is, some
                        portions of the sheet or strip are literally longer in some areas than others, trapped stresses
                        could be described as some portions of the sheet being under tension while other portions are
                        relaxed as illustrated on the stress-strain curve. The portions of the strip labeled C &amp; D
                        are under tension. While flatness and stresses are both clearly related, each problem is unique
                        and must be addressed as such.For leveling, the most important and key issue is the importance
                        of exceeding the material’s yield point in order to affect permanent change in the material.
                        Once you take the material past the yield point, everything before that is forgotten. While this
                        is the basic key requirement for producing flat strip, it is also the key to producing flat
                        material that stays flat.
                    </p>
                </div>
                <div class="w-1/3">
                    <img src="http://localhost/front-end/assests/imgs/stretcher-leveler-imgs/new1.jpg" alt="">
                    <p class="text-xs">While the material may appear to be flat and at rest, portions of the strip are
                        hung up on the stress-strain curve and are actually under tension.</p>
                </div>
            </div><br>
            <p class="text-sm">
                In order to get material flat, it is necessary to selectively elongate portions of the strip. If some
                sections of the strip are longer than others, the only way to get the material to lay flat is to
                sufficiently elongate the short portions lengthwise to “dimensionally equalize” the material. The goal
                is to make the strip dimensionally the same length across its entire width.
            </p><br>
            <p class="text-sm">
                In order to eliminate spring back, the strip must be “stress equalized” by elongating the material’s
                entire cross section, top to bottom, and side to side, past its yield point to erase its previous
                memory. As a result the randomly trapped internal stresses are made into consistently trapped stresses;
                that is, all the material is allowed to spring back consistently to the same point on the stress-strain
                curve. Consequently in order to produce flat material that stays flat, it is necessary to both elongate
                portions of the strip more than others to dimensionally equalize or flatten the strip while also
                elongating the entire strip enough to exceed the yield in all of the material to stress equalize the
                strip. Consequently it is important to note that it is possible to stress relieve material without
                necessarily getting it flat. Conversely it is possible to get material flat without eliminating trapped
                internal stresses.
            </p><br>

            <h3>ROLLER LEVELERS</h3>
            <img src="http://localhost/front-end/assests/imgs/stretcher-leveler-imgs/160315%20ROLLER%20LEVELER.jpg"
                width="200px" alt=""><br>

            <div class="flex">
                <div class="w-2/3 pr-2">
                    <p class="text-sm">
                        Roller Levelers bend the material progressively up and down over rolls of sufficient diameter to
                        stretch the outer and inner surfaces of the material past the yield point. The smaller the roll,
                        the more yielding top to bottom that will occur. The farther the outer surfaces are from the
                        central or “neutral” centerline, the more yielding that will occur. The “neutral” centerline or
                        fiber is an imaginary line or area in the middle of the material’s cross section which neither
                        stretches nor compresses during the bending process. As a result, the material in this neutral
                        area never exceeds its yield point. A Roller Leveler also has the ability to selectively stretch
                        the material from side to side. Roller Levelers incorporate individual backup roller flights or
                        banks that can be vertically adjusted so that the work rolls can be bent during the leveling
                        process. By deliberately bending the rolls, portions of the strip are forced to take a longer
                        path through the machine. As a result, some portions of the strip relative to length are
                        permanently stretched longer while other sections are stretched very little or not at all.
                    </p>
                </div>
                <div class="w-1/3">
                    <img src="http://localhost/front-end/assests/imgs/stretcher-leveler-imgs/new2.jpg" alt="">
                    <p class="text-xs">The sheet illustrates a part before leveling. The stress-strain curve illustrates
                        which portions of the sheet are stretched first and the greatest amount while also indicating
                        the portions of the material that have not been stretched past their yield point.</p>
                </div>
            </div><br>
            <p class="text-sm">
                It is also important to note that although portions of the strip have exceeded the yield point, much of
                the strip has not. Therein lies the problem. While the strip may be dimensionally equalized and allowed
                to lay flat, portions of the sheet are still in tension, unable to relax. Portions of the strip are in
                effect “hung up” and still under tension. Although it may appear as if the strip is at rest, portions of
                the material are actually under tension and being held in place by the surrounding material.<span
                    style="font-style: italic; text-decoration: underline;"> Once released by shearing or cutting the
                    material, these areas will simply spring back to their original often out of flat condition or
                    shape.</span>
            </p><br>
            <p class="text-sm">
                Today heavy duty Roller Levelers are being promoted as being able to equalize stress. While these units
                can certainly work the material much harder than conventional Roller Levelers, they cannot exceed the
                yield of the material throughout the entire thickness of the strip by simply bending the strip over a
                series of rolls. Nor can they assure that all the stresses in the material have been equalized side to
                side. Both are essential to producing low stress material. All Roller Levelers are also sensitive to
                incoming material shape. There is a limit to the amount of shape correction that can be expected by
                virtue of the amount of roll bend that is possible.
            </p>

            <h3>TEMPER MILLS</h3>
            <img src="http://localhost/front-end/assests/imgs/stretcher-leveler-imgs/160315%20TEMPER%20MILL.jpg"
                width="200px" alt=""><br>

            <div class="flex">
                <div class="w-2/3 pr-2">
                    <p class="text-sm">
                        Today, companies frequently use Temper Mills in Cut-To-Length Lines to produce stress relieved
                        material. As previously discussed, in order to eliminate randomly trapped internal stress, all
                        the material must be elongated or stretched beyond its yield point. A Temper Mill does this
                        through the use of compressive forces. By squeezing the material between two rolls with enough
                        force to reduce its thickness, the strip is slightly elongated. The typical amount of elongation
                        is only 1 or 2%. Although most Temper Mills have the ability to elongate the strip further,
                        doing so can change the material’s properties significantly. Consequently, some end users will
                        limit the allowable elongation their processor can impart due to these changes. While a Temper
                        Mill is often associated with producing flat material, to the contrary, the Mill by itself will
                        not necessarily get the material flat.
                    </p>
                </div>
                <div class="w-1/3">
                    <img src="http://localhost/front-end/assests/imgs/stretcher-leveler-imgs/new3.jpg" alt="">
                    <p class="text-xs">By squeezing the material between two rolls with enough force, the strip is
                        slightly elongated.</p>
                </div>
            </div><br>
            <p class="text-sm">
                Although the Mill elongates the entire cross section of the strip, the amount of elongation is
                essentially the same. For the most part, a Mill cannot selectively elongate the strip which is required
                for shape correction. As a result, it is possible to stress relieve material without necessarily getting
                it flat. Conventional Roller Levelers must still be used to flatten the material. This is why you will
                always find Roller Levelers being used in conjunction with a Temper Mill in Cut-To-Length Lines.
                However, by virtue of the roller leveling process, stresses can be reintroduced into the strip, and you
                can “undo” the benefits of the Temper Mill. If the Mill works the strip too hard, it will actually
                induce shape problems. If the Roller Leveler works the strip too much, the benefits of the Mill will be
                diminished or eliminated. This system is also sensitive to incoming strip shape. The old saying “junk
                in, junk out” is still true. While the combination of the Temper Mill and Roller Leveler will produce
                flat stress equalized material, the two systems are reliant on one another. In addition, the combination
                is still limited by the capabilities of the Roller Leveler relative to the severity of the incoming
                shape that can be corrected and the skill of the operators.
            </p>

            <h3>STRETCHER LEVELING</h3>

            <div class="flex">
                <div class="w-2/3 pr-2">
                    <p class="text-sm">
                        Unlike conventional Levelers, no bending is used as part of this leveling process. In-Line
                        Stretcher Levelers operate in conjunction with a Cut-To-Length Line. The strip is first
                        stretched and then subsequently cut-to-length to a specific sheet or blank size. An In-Line
                        Stretcher Leveler consists of a pair of entry and exit frames. These frames are adjustable
                        relative to the desired part or stretch length. When required, each frame grips the material
                        across its width. Large hydraulic cylinders connect the two frames. When pressurized, these
                        cylinders push the frames away from one another. The pressure exerted by the cylinders exceed
                        the collective yield of the material (thickness x width x yield), and the strip is subsequently
                        stretched in the direction of travel. The Line’s Feeding System pulls the material through the
                        Leveler incrementally. Between each Feed cycle, while the material is stopped to be sheared, a
                        portion of the strip is stretched.
                    </p>
                </div>
                <div class="w-1/3">
                    <img src="http://localhost/front-end/assests/imgs/stretcher-leveler-imgs/new4.jpg" alt="">
                    <p class="text-xs">While some portions of the strip are stretched more than others, all of the
                        material has exceeded its yield point and will “spring back” consistently.</p>
                </div>
            </div><br>

            <p class="text-sm">
                In addition to the material being sufficiently elongated lengthwise to exceed the yield point in all of
                the material, top to bottom, and edge to edge, by virtue of the process, portions of the strip are
                stretched more than others at the same time. In effect, the material’s entire cross section is stretched
                to a common length or distance; that length being sufficient to both dimensionally and stress equalize
                the material at the same time. No subsequent processing is required. The end result is material that is
                more homogeneous and consequently more stable than with other forms of leveling.<br> <br>

                While stretcher leveling is one of the oldest and most effective types of leveling ever developed, there
                is a
                limited understanding as to how the process works. Typically, there are concerns that the process works
                the material significantly more than with other types of leveling. Consequently, similar to the Temper
                Mill, if the material is overworked, this will change the properties of the material. However, while a
                Temper Mill may elongate the strip 1 or 2%, a Stretcher accomplishes its work with typically only .3 to
                .5% elongation. Relative to the percentage of elongation required to do its job, the Stretcher has very
                little impact on the metallurgical properties of the material. In regard to shape correction, if
                material has an edge wave or center buckle, the strip is longer in this area. Consequently, the short
                portions of the strip must be elongated to somewhat equal the longest length to allow the material to
                lay flat. However, regardless of the method used to elongate the strip, each system would need to
                elongate these areas essentially the same amount to achieve flat material. As a result, the percentage
                of elongation would be comparable. The Stretcher Leveler performs more overall work due to the fact the
                entire thickness of the material is elongated. However, no specific portion of the strip is worked more
                than with any other type of leveling process in order to achieve similar results. The Stretcher is
                relatively simple to adjust and operates with minimal skill. If the material is not flat, the operator
                simply increases the amount of stretch until the desired results are achieved. As long as the material
                lays flat, trapped internal stresses will also be consistent. Stretchers are also far less sensitive to
                incoming shape. Because Stretchers do not rely on the use of rolls, the ability to level is not limited
                by the amount of roll bend that is possible. As a result, the finished product is virtually independent
                of incoming shape.
            </p><br>
            <h3>IN CONCLUSION</h3>
            <p class="text-sm">
                Today, a number of companies offer systems that are promoted as producing temper passed or stretcher
                leveled “quality” material; however as to whether these systems actually achieve similar results are
                questionable. While there are a number of options available for leveling flat rolled material, each has
                their own inherent limitation. Consequently, in order to achieve the best possible results with a
                particular application, it is important to know the limitations of each machine.
            </p>
