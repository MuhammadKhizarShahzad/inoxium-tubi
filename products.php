            <h2>No. 1 Finish</h2>
            <img src="http://localhost/front-end/assests/imgs/products-imgs/gen-contact-image-thumb-18.jpg" alt="">
            <p><strong>No. 1 Finish:</strong> is produced by rolling stainless steel that has been heated prior to
                rolling (hot-rolling). This is followed by a heat treatment that produces a uniform microstructure
                (annealing) and ensures that the stainless steel will meet mechanical property requirements. After
                these processing steps, the surface has a dark non-uniform appearance called “scale”. Surface
                chromium has been lost during the previous processing steps, and, without removal of the scale, the
                stainless steel would not provide the expected level of corrosion resistance. Chemical removal of
                this scale is called pickling or descaling, and it is the final processing step. A No. 1 finish has
                rough, dull, and non-uniform appearance. There may be shiny spots were surface imperfections were
                removed by grinding. It is generally used in industrial applications, such as equipment for elevated
                temperature service.<br> <br><strong><span style="text-decoration: underline;">
                        Applications:</span></strong></p>
            <p>Air heaters, Annealing boxes, Boiler baffles, Carburizing boxes, Crystallizing pans, Firebox sheets,
                Furnace arch supports, Furnace conveyors, Furnace dampers, Furnace linings, Furnace stacks, Gas
                turbine parts, Heat exchanger baffles, Heat exchanger tubing supports, Incinerators, Industrial oven
                liners, Kiln liners, Oil burner parts, Recuperators, Refineries, Tube hangers.</p>
            <h2>2B Finish</h2>
            <img src="http://localhost/front-end/assests/imgs/products-imgs/gen-contact-image-thumb-19.jpg" alt="">
            <p><strong>2B Finish:</strong> is a bright cold rolled finish commonly produced in the same manner as
                No. 2D, except that the final light cold rolling pass is done using polished rolls. This produces a
                more reflective finish that resembles a cloudy mirror. Finish reflectivity can vary from
                manufacturer-to-manufacturer and coil-to-coil with some coils looking quite mirror-like and others
                being fairly dull. No. 2B is a general purpose cold rolled finish commonly used for all but
                exceptionally difficult deep drawing applications. It is more readily polished to high luster than a
                No. 1 or No. 2D finish.<br> <br><strong><span style="text-decoration: underline;">
                        Applications:</span></strong></p>
            <p>Bakeware, Chemical plant equipment, Dye house equipment, Flatware, Laundry and dry cleaning, Paper
                mill equipment, Pharmaceutical equipmentPlumbing fixtures, Refrigeration, Sewage treatment, Sheet
                metal products, Small tanks, Solar collector panels, Vacuum drum dryers, Waste fuel pool liners,
                Wheel covers.</p>
            <h2>Bright Annealed</h2>
            <img src="http://localhost/front-end/assests/imgs/products-imgs/gen-contact-image-thumb-20.jpg" alt="">
            <p><strong>Bright Annealed:</strong> is produced by heat-treating (annealing) in a controlled atmosphere
                furnace. It has a mirror like appearance but may have some cloudiness and other imperfections. A
                finish that is designated “BA” has only been bright annealed. A finish that is designated “2BA” has
                been bright annealed and then passed between highly polished rolls. A minimal amount of roll
                pressure (skin pass) is applied. The process improves flatness and finish uniformity but does not
                significantly decrease thickness. Bright annealed stainless is sometimes buffed to attain a more
                mirror-like finish. It is often specified for applications where a highly reflective surface is
                desired.<br> <br><strong><span style="text-decoration: underline;"> Applications: </span></strong>
            </p>
            <p>Appliance trim, Appliances, Architectural components, Auto trim, Builder’s hardware, Chute liners,
                Commercial refrigerators, Cookware, Fishing equipment, Flatware, Food processing equipment, General
                hardware, Highway truck trailers, Mass transit cars, Radiant heaters, Range hoods, Scientific
                apparatus, Surgical instruments, Textile finishing equipment.</p>
            <h2>No. 4 Finish:</h2>
            <img src="http://localhost/front-end/assests/imgs/products-imgs/gen-contact-image-thumb-21.jpg" alt="">
            <p>No. 4 Finish: Finish is characterized by short, parallel polishing lines, which extend uniformly
                along the length of the coil. It is obtained by mechanically polishing a No. 3 finish with gradually
                finer abrasives. Depending on the requirements of the application, the final finish can be anywhere
                between 120 and 320 grit. Higher grit numbers produce finer polishing lines and more reflective
                finishes. The surface roughness is typically Ra 25 micro-inches or less. This general-purpose finish
                is widely used for restaurant and kitchen equipment, storefronts and food processing and dairy
                equipment. If a fabricator needs to blend in welds or do other refinishing, the resulting polishing
                lines are usually longer than on product polished by a producer or toll-polishing house.<br>
                <br><strong><span style="text-decoration: underline;"> Applications: </span></strong>
            </p>
            <p>Appliances, Architectural wall panels, Beverage equipment, Boat fittings, Bus shelters, Clean rooms,
                Column covers, Dairy equipment, Elevator doors and interiors, Escalator trim, Food processing
                equipment, Furniture Highway tank trailers, Hospital surfaces and equipment, Instrument or control
